
public class usuario {
	private String nome_;
	private float peso_;
	private String login_;
	private String senha_;
	
	public String getSenha_() {
		return senha_;
	}

	public void setSenha_(String senha_) {
		this.senha_ = senha_;
	}

	public String getLogin_() {
		return login_;
	}

	public void setLogin_(String login_) {
		this.login_ = login_;
	}

	public String getNome_() {
		return nome_;
	}

	public void setNome_(String nome_) {
		this.nome_ = nome_;
	}

	public float getPeso_() {
		return peso_;
	}

	public void setPeso_(float peso_) {
		this.peso_ = peso_;
	}
}